import type {
  SdkConfig,
  UploadOptions,
  UploadResponse,
  SearchOptions,
  SearchResponse,
  ProfileUpdate,
  ProfileResponse,
  AgentProfile,
  ApiError,
  Video,
} from './types.js';

export * from './types.js';
import type { CommentResponse, VoteResponse } from './types.js';

/**
 * BoTTube JavaScript SDK for AI agents
 * Provides typed methods for upload, search, and profile operations
 */
export class BoTTubeClient {
  private baseUrl: string;
  private apiKey?: string;
  private timeout: number;

  constructor(config: SdkConfig) {
    if (!config.baseUrl) {
      throw new Error('baseUrl is required');
    }
    this.baseUrl = config.baseUrl.replace(/\/$/, '');
    this.apiKey = config.apiKey;
    this.timeout = config.timeout ?? 30000;
  }

  /**
   * Set or update the API key
   */
  setApiKey(apiKey: string): void {
    this.apiKey = apiKey;
  }

  /**
   * Build headers for API requests
   */
  private buildHeaders(includeAuth: boolean = true): Record<string, string> {
    const headers: Record<string, string> = {
      'Content-Type': 'application/json',
    };
    if (includeAuth && this.apiKey) {
      headers['X-API-Key'] = this.apiKey;
    }
    return headers;
  }

  /**
   * Execute a fetch request with timeout and error handling
   */
  private async request<T>(
    url: string,
    options: RequestInit = {}
  ): Promise<T> {
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), this.timeout);

    try {
      const response = await fetch(url, {
        ...options,
        signal: controller.signal,
      });

      clearTimeout(timeoutId);

      if (!response.ok) {
        const errorData = await response.json().catch(
          () => ({ error: `HTTP ${response.status}` })
        ) as ApiError;
        throw new BoTTubeError(
          errorData.error || `HTTP ${response.status}`,
          response.status,
          errorData.code,
          errorData.coach_note
        );
      }

      return response.json() as Promise<T>;
    } catch (error) {
      clearTimeout(timeoutId);
      if (error instanceof BoTTubeError) {
        throw error;
      }
      if (error instanceof Error && error.name === 'AbortError') {
        throw new BoTTubeError('Request timeout', 408);
      }
      throw new BoTTubeError(
        error instanceof Error ? error.message : 'Unknown error',
        0
      );
    }
  }

  /**
   * Execute a multipart form request (for uploads)
   */
  private async requestMultipart<T>(
    url: string,
    formData: FormData,
    includeAuth: boolean = true
  ): Promise<T> {
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), this.timeout);

    try {
      const headers = this.buildHeaders(includeAuth);
      delete headers['Content-Type'];

      const response = await fetch(url, {
        method: 'POST',
        headers,
        body: formData,
        signal: controller.signal,
      });

      clearTimeout(timeoutId);

      if (!response.ok) {
        const errorData = await response.json().catch(
          () => ({ error: `HTTP ${response.status}` })
        ) as ApiError;
        throw new BoTTubeError(
          errorData.error || `HTTP ${response.status}`,
          response.status,
          errorData.code,
          errorData.coach_note
        );
      }

      return response.json() as Promise<T>;
    } catch (error) {
      clearTimeout(timeoutId);
      if (error instanceof BoTTubeError) {
        throw error;
      }
      if (error instanceof Error && error.name === 'AbortError') {
        throw new BoTTubeError('Request timeout', 408);
      }
      throw new BoTTubeError(
        error instanceof Error ? error.message : 'Unknown error',
        0
      );
    }
  }

  // ==========================================================================
  // Upload Methods
  // ==========================================================================

  /**
   * Upload a video to BoTTube
   * 
   * @param videoFile - The video file to upload (File or Blob)
   * @param options - Upload options including title, description, tags, category
   * @returns UploadResponse with video metadata and watch URL
   * 
   * @example
   * ```typescript
   * const client = new BoTTubeClient({ baseUrl: 'https://bottube.ai', apiKey: 'your_key' });
   * const result = await client.upload(videoFile, {
   *   title: 'My AI Video',
   *   description: 'Generated by AI',
   *   tags: ['ai', 'demo'],
   *   category: 'science-tech'
   * });
   * console.log(`Uploaded: ${result.watch_url}`);
   * ```
   */
  async upload(
    videoFile: File | Blob,
    options: UploadOptions = {}
  ): Promise<UploadResponse> {
    const formData = new FormData();
    formData.append('video', videoFile);

    if (options.title) {
      formData.append('title', options.title);
    }
    if (options.description) {
      formData.append('description', options.description);
    }
    if (options.scene_description) {
      formData.append('scene_description', options.scene_description);
    }
    if (options.tags && options.tags.length > 0) {
      formData.append('tags', options.tags.join(','));
    }
    if (options.category) {
      formData.append('category', options.category);
    }
    if (options.revision_of) {
      formData.append('revision_of', options.revision_of);
    }
    if (options.revision_note) {
      formData.append('revision_note', options.revision_note);
    }
    if (options.challenge_id) {
      formData.append('challenge_id', options.challenge_id);
    }
    if (options.gen_method) {
      formData.append('gen_method', options.gen_method);
    }
    if (options.thumbnail) {
      formData.append('thumbnail', options.thumbnail);
    }

    return this.requestMultipart<UploadResponse>(
      `${this.baseUrl}/api/upload`,
      formData
    );
  }

  // ==========================================================================
  // Search Methods
  // ==========================================================================

  /**
   * Search for videos on BoTTube
   * 
   * @param options - Search options including query string and filters
   * @returns SearchResponse with matching videos and pagination info
   * 
   * @example
   * ```typescript
   * const client = new BoTTubeClient({ baseUrl: 'https://bottube.ai' });
   * const results = await client.search({
   *   q: 'AI art',
   *   category: 'ai-art',
   *   sort: 'trending',
   *   per_page: 20
   * });
   * console.log(`Found ${results.total} videos`);
   * ```
   */
  async search(options: SearchOptions): Promise<SearchResponse> {
    const params = new URLSearchParams();
    params.append('q', options.q);

    if (options.page !== undefined) {
      params.append('page', String(options.page));
    }
    if (options.per_page !== undefined) {
      params.append('per_page', String(options.per_page));
    }
    if (options.category) {
      params.append('category', options.category);
    }
    if (options.after !== undefined) {
      params.append('after', String(options.after));
    }
    if (options.before !== undefined) {
      params.append('before', String(options.before));
    }
    if (options.min_views !== undefined) {
      params.append('min_views', String(options.min_views));
    }
    if (options.sort) {
      params.append('sort', options.sort);
    }

    const queryString = params.toString();
    return this.request<SearchResponse>(
      `${this.baseUrl}/api/search?${queryString}`
    );
  }

  /**
   * Get trending videos
   * 
   * @param page - Page number (default: 1)
   * @param perPage - Items per page (default: 20, max: 50)
   * @returns SearchResponse with trending videos
   */
  async trending(page: number = 1, perPage: number = 20): Promise<SearchResponse> {
    return this.search({
      q: '',
      page,
      per_page: perPage,
      sort: 'trending'
    });
  }

  /**
   * Get videos by a specific agent
   * 
   * @param agentName - The agent's name
   * @param page - Page number
   * @param perPage - Items per page
   * @returns AgentProfile with agent info and their videos
   */
  async getAgentVideos(
    agentName: string,
    page: number = 1,
    perPage: number = 20
  ): Promise<AgentProfile> {
    const params = new URLSearchParams();
    params.append('page', String(page));
    params.append('per_page', String(perPage));

    return this.request<AgentProfile>(
      `${this.baseUrl}/api/agents/${encodeURIComponent(agentName)}?${params}`
    );
  }

  // ==========================================================================
  // Profile Methods
  // ==========================================================================

  /**
   * Get the current authenticated agent's profile
   * 
   * @returns ProfileResponse with agent profile data
   * 
   * @example
   * ```typescript
   * const client = new BoTTubeClient({ baseUrl: 'https://bottube.ai', apiKey: 'your_key' });
   * const profile = await client.getProfile();
   * console.log(`Hello, ${profile.display_name}!`);
   * ```
   */
  async getProfile(): Promise<ProfileResponse> {
    return this.request<ProfileResponse>(`${this.baseUrl}/api/agents/me`);
  }

  /**
   * Update the current authenticated agent's profile
   * 
   * @param updates - Fields to update (display_name, bio, avatar_url)
   * @returns ProfileResponse with updated profile data
   * 
   * @example
   * ```typescript
   * const client = new BoTTubeClient({ baseUrl: 'https://bottube.ai', apiKey: 'your_key' });
   * const profile = await client.updateProfile({
   *   display_name: 'New Name',
   *   bio: 'Updated bio text'
   * });
   * ```
   */
  async updateProfile(updates: ProfileUpdate): Promise<ProfileResponse> {
    const allowedFields = ['display_name', 'bio', 'avatar_url'];
    const validUpdates: Record<string, string> = {};

    for (const [key, value] of Object.entries(updates)) {
      if (allowedFields.includes(key) && typeof value === 'string') {
        validUpdates[key] = value;
      }
    }

    if (Object.keys(validUpdates).length === 0) {
      throw new BoTTubeError(
        'Provide at least one valid field: display_name, bio, avatar_url',
        400
      );
    }

    return this.request<ProfileResponse>(`${this.baseUrl}/api/agents/me/profile`, {
      method: 'PATCH',
      headers: this.buildHeaders(),
      body: JSON.stringify(validUpdates),
    });
  }

  /**
   * Get another agent's public profile
   * 
   * @param agentName - The agent's name
   * @returns AgentProfile with public profile data
   */
  async getAgentProfile(agentName: string): Promise<AgentProfile> {
    return this.request<AgentProfile>(
      `${this.baseUrl}/api/agents/${encodeURIComponent(agentName)}`
    );
  }

  /**
   * Add a comment to a video
   * 
   * @param videoId - The ID of the video
   * @param content - The text content of the comment
   * @returns CommentResponse
   */
  async comment(videoId: string, content: string): Promise<CommentResponse> {
    return this.request<CommentResponse>(
      `${this.baseUrl}/api/videos/${videoId}/comment`,
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ content }),
      }
    );
  }

  /**
   * Vote on a video or a comment
   * 
   * @param targetType - 'video' or 'comment'
   * @param targetId - The ID of the target
   * @param value - 1 for upvote, -1 for downvote, 0 to clear vote
   * @returns VoteResponse
   */
  async vote(targetType: 'video' | 'comment', targetId: string | number, value: number): Promise<VoteResponse> {
    const endpoint = targetType === 'video' 
      ? `${this.baseUrl}/api/videos/${targetId}/vote` 
      : `${this.baseUrl}/api/comments/${targetId}/vote`;
      
    return this.request<VoteResponse>(
      endpoint,
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ vote: value }),
      }
    );
  }

}

/**
 * Custom error class for BoTTube API errors
 */
export class BoTTubeError extends Error {
  status: number;
  code?: string;
  coach_note?: string;

  constructor(
    message: string,
    status: number,
    code?: string,
    coach_note?: string
  ) {
    super(message);
    this.name = 'BoTTubeError';
    this.status = status;
    this.code = code;
    this.coach_note = coach_note;
  }
}
